                    <h4>1. What is an "earthmile"?</h4>
                    <p>Put simply, an earthmile is a unit for the point system we use to reward you for your healthy, earth-loving, green behaviours. </p>

                    <h4>2. How do I get earthmiles?</h4>
                    <p>Very easily – when you register on our app, you will be taken to a set up page where you can link your account to a tracking app that you use. Once you link it, we will automatically pull your activities data from your apps and award you with earthmiles! Keep checking your profile for your latest earthmiles balance.</p>

					<h4>3. What gets me earthmiles?</h4>
                    <p>Today we can award you earthmiles for walking, running, and cycling. A healthier you is the first step to a healthier, happier world! But watch this space closely as we have exciting plans of expanding the scope of what earns earthmiles, including fitness, health-related, community-oriented and environmentally positive behaviours.</p>

					<h4>4. Help! The app I use to track my walking / running / cycling is not on the list on the set up page.</h4>
                    <p>Don’t worry! We are quickly linking to more and more apps to make sure your favourite tracking apps are included. In the meantime if you want to check out the current rewards, you could give the available apps a try.</p>

					<h4>5. What happens when I use two apps – do I win double earthmiles?</h4>
                    <p>Sorry, no. We will be removing the duplication on the basis of the activity time stamp, location and duration.</p>

					<h4>6. Are there any checks on how I get my activities done?</h4>
                    <p>Yes, indeed. We check for the following issues and if we spot any erratic data coming from you we will flag it to you before removing it from your earthmiles score.</p>
                    <ul class="faq-ul">
                    <li>Walking is trackable throughout the day; running and cycling data is limited to once a day</li>

					<li>From any one user account we will only accept data from one country on any given day; alternating locations would give rise to a red flag</li>

					<li>The data is cleaned for lazy runs and any stray data picked up while moving in vehicles. If you disagree with our deletions, let us know and someone will get in touch to discuss</li>
					</ul>

					<h4>7. How do you convert my activities into earthmiles?</h4>
                    <p>We have a proprietary model based on extensive user surveys on the basis of which we convert user activity data into earthmiles. While in beta mode, we will be refining these conversion ratios so you might see some changes in the number of earthmiles your activity gets you</p>

					<h4>8. What is the difference between credit available and earthmiles?</h4>
                    <p>On the rewards centre you will see a number for “credit available”. This is the total number of earthmiles you have remaining, reduced by all the earthmiles you have already redeemed on the rewards centre. Your friends and everyone will see your earthmiles balance, which is all the earthmiles you have earned so far, including any you have spent. It’s a number to be proud of!</p>

					<h4>9. Can I see what my friends are doing on earthmiles?</h4>
                    <p>Absolutely! You can search and add your friends from facebook. You will then be able to see their activity feed which will show up on your home page, and also visit their profile where you can check out how many earthmiles they have.</p>

					<h4>10. Can I invite a friend who is not currently using earthmiles?</h4>
                    <p>Of course! You can send them an email invitation. If your friend joins and mentions you as a referral, we will award you and them 50 earthmiles as a bonus!</p>

					<h4>11. Why do you need me to fill out the About Me section?</h4>
                    <p>It’s useful to have your basic info on your profile page so your friends can identify you. We ask for some necessary information such as your name, age, location etc., so we can ensure the working of the app is smooth for you. We also have some optional data for you to fill when you set up your profile, which helps us direct better more suitable rewards to  you and helps our partners know what sort of users like their rewards. Your personal data is safe with us – you can read the full privacy statement here.</p>

					<h4>12. What sort of rewards can I expect to see on the rewards centre?</h4>
                    <p>Earthmiles is all about health, the environment and the community. You will see fantastic rewards that sit within our philosophy of creating a healthier happier world.</p>

					<h4>13. Can I delete my account?</h4>
